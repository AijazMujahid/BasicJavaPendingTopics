package com.mujahid.flowControl;

public class P19_SwitchCase {

	public static void main(String[] args) {
		int choice = 130;
		switch(choice) {
		
		default : System.out.println("this is default");
		break;
		case 10 : System.out.println("this is 10");
		System.out.println("this is again 10");
		System.out.println("this is again 10");
		break;
		case 11 : System.out.println("this is 11");
		System.out.println("this is again 11");
		break;
		
		}

	}

}
